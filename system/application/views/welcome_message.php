<h3>Background Information</h3>
<p>
    This project is the result of a two-day site audit of Piedmont Furnishings sales force process. This audit concluded that their sales process
    could benefit greatly from a web-based site for reporting and monitoring sales activity.  This project will include security features, use of
    a central database for consistency, real-time placement of orders, and print quality reports &amp; graphics.
</p>
<h3>Project Goals</h3>
<p>
    This project will implement various web-technologies to reach the following goals outlined by the audit:
</p>
<ul>
    <li>
        Reduce delay between placement of orders by customers &amp; actual processing of orders.
    </li>
    <li>
        Eliminate manual paper submission of new sales orders &amp; updates to existing orders.
    </li>
    <li>
        Eliminate manual creation and tailoring of reports.
    </li>
    <li>
        Provide restricted administrative access to detailed sales information.
    </li>
    <li>
        Utilize central data store to enable comparative performance analyses.
    </li>
</ul>
<p>
    If all of these goals are reached, there will be noticeable increases in the profits of Piedmond Furnishings.
</p>
